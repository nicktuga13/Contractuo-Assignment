    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //grabs user data and assigns it to $user
        $user = Auth::user();

        //check if the ammount of tasks created by the user is less than 5 in order to create a new task
        if(Task::where('user_id', $user->id)->count() < 5){
            $validated = $request->validate([
                'task' => 'required|string|max:255',
            ]);

            $user->tasks()->create($validated);
        }
        
        //returns response based on if a new task was created or not
        else{
            return response()->json('Too Many Tasks Created!');
        }
        return response()->json('Task Created!');
    }

